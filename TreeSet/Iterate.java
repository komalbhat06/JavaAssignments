package com.example;

import java.util.TreeSet;

public class Iterate {
		public static void main(String[] args) {
				TreeSet<String> colors= new TreeSet<>();
				colors.add("RED");
				colors.add("PINK");
				colors.add("BROWN");
				colors.add("ORANGE");
				for (String i:colors) {
					System.out.println(i);
				}
		}
		}

